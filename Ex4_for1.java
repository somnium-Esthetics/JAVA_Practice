public class Ex4_for1 {

	public static void main(String[] args) {
		for (int i =1; i<=5; i++) {
			System.out.println("i="+i);
		}
		for (int i = 5; i>=1; i--) {
			System.out.println("i="+i);
		}
		
		//1~10 홀수
		for (int a =1 ; a<=10; a=a+2) {
			System.out.println("a="+a);
		}
		
		//10~1 짝수
		for (int b =10; b>=1; b=b-2)
		{System.out.println("b="+b);

	}

}}
